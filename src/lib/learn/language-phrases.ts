/** The five course slugs that have aligned phrase text. */
type PhraseLang = "en" | "th" | "zh" | "ja" | "ko";

const SLUG_TO_LANG: Record<string, PhraseLang> = {
  english: "en",
  thai: "th",
  chinese: "zh",
  japanese: "ja",
  korean: "ko",
};

/**
 * Build the twenty useful-phrase units for one course, picking the right
 * target/roman column for that language. Returns `[]` for an unknown course so
 * callers can safely spread the result.
 */
export function phraseUnitsFor(courseSlug: string): LangUnit[] {
  const lang = SLUG_TO_LANG[courseSlug];
  if (!lang) return [];
  return PHRASE_LESSONS.map((lesson) => ({
    slug: lesson.slug,
    title: lesson.title,
    subtitle: lesson.subtitle,
    emoji: lesson.emoji,
    items: lesson.rows.map<Phrase>((row) => {
      const [target, roman] = row[lang];
      return { target, roman, my: row.my, emoji: row.emoji };
    }),
  }));
}
