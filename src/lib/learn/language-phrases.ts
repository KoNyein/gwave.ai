// Twenty "useful phrases" lessons, shared across every language course.
//
// Each phrase is written once by *meaning* (Burmese + emoji) with the target
// text and a pronunciation hint for all five languages side by side, so the
// English/Thai/Chinese/Japanese/Korean lessons always stay in sync. The builder
// below turns them into per-course `LangUnit`s that plug straight into the same
// listen / speak / type trainer used by the vocabulary units.
//
// Romanisation convention (matches the existing units):
//   • English  — Burmese-script phonetic hint (for a Burmese reader)
//   • Thai / Chinese / Japanese / Korean — Latin transliteration (RTGS / pinyin
//     with tone marks / rōmaji / Revised Romanisation)

import type { LangUnit, Phrase } from "./languages";

/** [target text, pronunciation hint] for one language. */
type L = [target: string, roman: string];

/** One phrase, defined by meaning and given in all five languages. */
